TITLE NOME:VICTOR DE MELO ROSTON | RA:22006737

.MODEL SMALL
SAI_DOS MACRO
    MOV AH,4CH
    INT 21H
    ENDM

.DATA
    QTDE EQU 8
    STR1 DB 'OSC 2522'
    STR2 DB QTDE DUP (?)
    
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV ES, AX

    CALL INV
    CALL IMP

    SAI_DOS
MAIN ENDP


INV PROC
    LEA SI, STR1+7
    LEA DI, STR2
    STD
    MOV CX, QTDE
 INVERTE:
    MOVSB
    ADD DI, 2
    LOOP INVERTE
    RET
INV ENDP


IMP PROC
    CLD
    MOV CX, QTDE
    LEA SI, STR2
    MOV AH, 02
 IMPRIME:
    LODSB
    MOV DL, AL
    INT 21H
    LOOP IMPRIME
    RET
IMP ENDP

END MAIN